    <h5 class="card-title">Schaken voor Kids</h5>
    <p class="card-text">Clubs en scholen
Clubs en scholen krijgen na aanmelding een eigen plek binnen de Online Jeugdschaakclub Nederland onder groepen. Een volwassene (de contactpersoon) krijgt het beheer over de groep en kan de eigen kinderen toegang geven tot deze groep.
Hierbij een overzicht van de te doorlopen stappen:

1)Aanmelden bij de KNSB om voordelig goldaccounts voor Chesskid in te kopen;
Hier wordt de naam en adres van de school gevraagd. Kort de schoolnaam eventueel af naar onder de 25 karakters. De naam van de school wordt namelijk ook gebruikt in de gebruikersnamen van de kinderen. Het opgegeven emailadres van de contactpersoon (moet een volwassene zijn) wordt tevens gebruikt voor de toegang tot Chesskid. 
Van de kinderen wordt de voornaam, tussenvoegsel, achternaam en geboorte datum gevraagd. De voornaam komt ook terug in de gebruikersnaam. Een volledige naam en geboorte datum is nodig voor de identificatie en registratie in de administratie als KNSB scholierenlid.
De gebruikersnamen van de kinderen worden door de schaakbond gegenereerd op basis van de school of clubnaam en de voornaam.

2) De KNSB zal de contactpersoon vervolgens uitnodigen voor Chesskid als beheerder van de groep. Valideer het account door op de link in de toegestuurde email te klikken. U kunt nu voor uzelf een account aanmaken met een zelfgekozen avatar (plaatje). Als u al een account bij Chesskid had, dan wordt deze automatisch  aan de nieuwe groep gekoppeld. 
De naam van de groep bestaat uit het schoolnummer of clubnummer gevolgd door de school- of clubnaam. Kinderen kunnen van meerdere groepen lid zijn (bv een school en een club). 

3) De KNSB zal zorgen dat het aantal ingekochte accounts voor de kinderen worden geactiveerd. Let wel het jaarlidmaatschap van alle aangevraagde accounts  gaat direct lopen vanaf het moment van aanvragen, tenzij een andere activatiedatum is gecommuniceerd. Er kunnen dus geen accounts worden opgespaard voor in de toekomst. Dit heeft te maken met de bulk inkoopprijs.

4) Voor je groep biedt Chesskid een paar hulpmiddelen om de logins aan de eigen kinderen te verstrekken. Login in Chesskid en ga naar je groep.
Aan de rechterzijde van het menu staat het volgende:

· Print login kaartjes voor de leerlingen met hun usernaam & wachtwoord.
· Plaats een aankondiging  inclusief een link naar wat je je leerlingen wilt laten doen zodra ze zijn ingelogd.
5) Alle via de KNSB aangemelde scholen, clubs en kinderen worden automaticsh lid van de club Online Jeugdschaakclub Nederland. Daarnaast zullen de komende tijd nog extra deelclubs worden ingericht. Clubs binnen Chesskid betekenen toernooien of competities. Kinderen kunnen lid worden van deze clubs om uitnodigingen te ontvangen. Clubs kunnen bepaalde eisen stellen biijvoorbeeld een bepaalde rating grens (onder of boven) of een bepaalde groep op een school. 

6) Op Chesskid kunnen clubs en scholen de eigen kinderen laten schaken:

tegen kinderen van de eigen school / club
tegen kinderen van andere scholen en clubs via de door de KNSB georganiseerde schaaktoernooien
tegen de computer
tegen kinderen over de hele wereld (snel- of live schaken) die op dat moment ook online zijn
Voor de kinderen:

Login in met de ontvangen login gegevens; 
Kies een leuke eigen avatar;
Je kunt je wachtwoord zelf wijzigen. Je gebruikersnaam is door de schaakbond gegenereerd, deze kan niet gewijzigd worden;
Om te schaken heb je 4 mogelijkheden:
1) Snelschaken tegen iemand die ook online is (5, 10 of 15 minuten). Wil je tegen een vriend(in) of clubgenoot schaken, kies dan het tabblad vrienden of clubgenoten. Vrienden kun je zelf verzamelen door ze uit te nodigen of vrienden verzoeken te accepteren.
2) Langzaam schaak. Bijvoorbeeld dat je 1 zet per dag doet of zelfs langer. Hier kun je ook kiezen tussen een vriend(in) of een clubgenoot.
3) Deelnemen aan toernooien. Deze worden apart aangekondigd voor een bepaalde datum en tijd. Kies meedoen om je aan te melden (binnenkort). 
4) Schaken tegen de computer. Voordeel is altijd een tegenstander van gelijk niveau.
Je kunt sterren sparen door veel te schaken, puzzels op te lossen en toernooien te spelen. Natuurlijk krijg je ook sterren als je wint, maar veel schaken levert minstens zoveel sterren op.
Alvast heel veel plezier!

